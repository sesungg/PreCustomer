write_dockerfile() {
  local repo_dir="$1"
  local archive_name="$2"

  cat > "${repo_dir}/Dockerfile" <<DOCKER
FROM eclipse-temurin:17-jre

ARG JAR_FILE=build/libs/${archive_name}-0.0.1-SNAPSHOT.jar

RUN apt-get update \\
    && apt-get install -y --no-install-recommends curl \\
    && rm -rf /var/lib/apt/lists/*

WORKDIR /app
COPY \${JAR_FILE} /app/app.jar

ENTRYPOINT ["java", "-jar", "/app/app.jar"]
DOCKER
}

write_service_ci() {
  local repo_dir="$1"
  local workflow_name="$2"
  local gradle_tasks="$3"

  mkdir -p "${repo_dir}/.github/workflows"
  cat > "${repo_dir}/.github/workflows/ci.yml" <<YAML
name: ${workflow_name}

on:
  push:
  pull_request:

jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - name: Checkout service
        uses: actions/checkout@v4
      - name: Checkout contracts sibling
        uses: actions/checkout@v4
        with:
          repository: \${{ vars.CONTRACTS_REPOSITORY || 'sesungg/precustomer-contracts' }}
          path: precustomer-contracts
      - name: Link contracts as sibling for composite build
        run: ln -s "\$PWD/precustomer-contracts" ../precustomer-contracts
      - uses: actions/setup-java@v4
        with:
          distribution: temurin
          java-version: '17'
      - run: ./gradlew ${gradle_tasks}
YAML
}

