./gradlew bootJar
\`\`\`
README

  write_service_ci "${repo_dir}" "service-ci" "bootJar"

  init_repo \
    "${repo_dir}" \
    "feat: ${root_name} 서비스 repo 분리" \
    "공통 core 없이 ${root_name} 실행 앱과 Dockerfile을 독립 repo로 분리했다.

Constraint: 서비스 repo는 contracts 외 공통 라이브러리에 의존하지 않아야 함
Confidence: high
Scope-risk: moderate
Tested: generated by tools/export-multi-repo-msa.sh"
}

