write_service_repo() {
  local source_name="$1"
  local repo_name="$2"
  local root_name="$3"
  local main_class="$4"
  local archive_name="$5"
  local profiles="$6"
  local description="$7"
  local repo_dir="${TARGET_DIR}/${repo_name}"

  copy_gradle_scaffold "${repo_dir}"
  write_settings "${repo_dir}" "${root_name}" "true"
  cp -R "${ROOT_DIR}/apps/${source_name}/src" "${repo_dir}/src"

  if [[ "${source_name}" == "report-worker" ]]; then
    mkdir -p "${repo_dir}/scripts"
    cp -R "${ROOT_DIR}/scripts/pipeline" "${repo_dir}/scripts/"
  fi

  write_service_build_gradle "${repo_dir}" "${main_class}" "${archive_name}" "${profiles}"
  write_dockerfile "${repo_dir}" "${archive_name}"

  cat > "${repo_dir}/README.md" <<README
# ${root_name}

${description}

## Local

