#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
TARGET_DIR="${1:-"${ROOT_DIR}/../PreCustomerMSA"}"

if [[ -e "${TARGET_DIR}" ]] && [[ -n "$(find "${TARGET_DIR}" -mindepth 1 -maxdepth 1 -print -quit 2>/dev/null)" ]]; then
  echo "Target directory is not empty: ${TARGET_DIR}" >&2
  echo "Choose a new path or remove it yourself before exporting." >&2
  exit 1
fi

mkdir -p "${TARGET_DIR}"

copy_gradle_scaffold() {
  local repo_dir="$1"
  mkdir -p "${repo_dir}"
  cp -R "${ROOT_DIR}/gradle" "${repo_dir}/"
  cp "${ROOT_DIR}/gradlew" "${repo_dir}/"
  cp "${ROOT_DIR}/gradlew.bat" "${repo_dir}/"
  cp "${ROOT_DIR}/.gitignore" "${repo_dir}/"
}

init_repo() {
  local repo_dir="$1"
  local subject="$2"
  local body="$3"

  (
    cd "${repo_dir}"
    if git init -b main >/dev/null 2>&1; then
      :
    else
      git init >/dev/null
      git checkout -B main >/dev/null
    fi
    git add -A
    git -c user.name="PreCustomer Export" \
      -c user.email="precustomer@example.invalid" \
      commit -m "${subject}" -m "${body}" >/dev/null
  )
}

write_settings() {
  local repo_dir="$1"
  local root_name="$2"
  local include_contracts="$3"

  cat > "${repo_dir}/settings.gradle" <<SETTINGS
pluginManagement {
    repositories {
        gradlePluginPortal()
        mavenCentral()
    }
}

dependencyResolutionManagement {
    repositoriesMode.set(RepositoriesMode.FAIL_ON_PROJECT_REPOS)
    repositories {
        mavenCentral()
        mavenLocal()
    }
}

rootProject.name = '${root_name}'
SETTINGS

  if [[ "${include_contracts}" == "true" ]]; then
    cat >> "${repo_dir}/settings.gradle" <<'SETTINGS'

def localContracts = file('../precustomer-contracts')
if (localContracts.isDirectory()) {
    includeBuild(localContracts)
}
SETTINGS
  fi
}

