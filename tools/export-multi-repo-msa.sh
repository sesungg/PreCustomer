./gradlew bootRun
./gradlew bootJar
java -jar build/libs/${archive_name}-0.0.1-SNAPSHOT.jar --spring.profiles.active=${profiles}
\`\`\`

contracts repo가 다른 위치에 있다면 먼저 Maven local에 publish합니다.

\`\`\`bash
